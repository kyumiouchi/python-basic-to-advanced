"""
None Type

None represents no type, or empty (APPROPRIATE).

Correct: None
Incorrect: none

OBS: None is ALWAYS False
"""

numbers = None
print(numbers)  # None
print(type(numbers))  # <class 'NoneType'>

numbers = 1.44, 3.10, 5.67

print(numbers)  # (1.44, 3.1, 5.67)
print(type(numbers))  # <class 'tuple'>

